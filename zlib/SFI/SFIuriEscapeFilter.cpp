COLuint32 SFIuriEscapeFilter::write(const void* pBuffer, COLuint32 SizeOfBuffer) {
   return write(pBuffer, SizeOfBuffer, true, false);
}

COLuint32 SFIuriEscapeFilter::write(const void* pBuffer, COLuint32 SizeOfBuffer, bool SpacesToPlusses, bool Generous) {
   COLsink& Output = next();
   unsigned char* pDontEscapeTable = Generous ? s_DontEscapeLookupTableGenerous : s_DontEscapeLookupTable;
   const unsigned char* Unescaped = (const unsigned char*)pBuffer;
   int UnescapedLength = SizeOfBuffer;
   for (int i = 0; i < UnescapedLength; ++i) {
      unsigned char c = Unescaped[i];
      unsigned char CharBuffer[4];
      int CharBufferLength = 1;
      if (pDontEscapeTable[(unsigned char)c]) {
         CharBuffer[0] = c;
      } else if (c == ' ' && SpacesToPlusses) {
         CharBuffer[0] = '+';
      } else {
         static const char hex[] = "0123456789ABCDEF";
         unsigned char u = (unsigned char)c;
         CharBuffer[0] = '%';
         CharBuffer[1] = hex[u >> 4];
         CharBuffer[2] = hex[u & 0xf];
         CharBufferLength = 3;
      }
      Output.write(CharBuffer, CharBufferLength);
   }
   return SizeOfBuffer;
}

