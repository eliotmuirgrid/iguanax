COLstring SFIuriEscape(const COLstring& Input, bool SpacesToPlusses, bool Generous){
   COLstring Output;
   COLsinkString Sink(Output);
   SFIuriEscapeFilter Filter(&Sink);
   Filter.write(Input.c_str(), Input.size(), SpacesToPlusses, Generous);
   return Output;
}
